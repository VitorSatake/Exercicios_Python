# declaração das variáveis do programa
# A, B, C, D, são declaradas na atribuição de valores

# atribuiu o valor 0 a variável D

D = 0
# exibe na tela o valor de D com frase explicativa

print("Valor de D :", D)

# atribuiu o valor 2 a variável A

A = 2
# exibe na tela o valor de A com frase explicativa

print("Valor de A :", A)

# atribuiu o valor 3 a variável B

B = 3
# exibe na tela o valor de B com frase explicativa

print("Valor de B :", B)

# atribuiu o valor 5 a variável C

C = 5
# exibe na tela o valor de C com frase explicativa

print("Valor de C :", C)

#atualiza a variável C o seu valor anterior acrescido de 1

C = C + 1

# exibe na tela o valor de C (atualizado) com frase axplicativa

print("Valor de C :", C)

# atribui a variável D a soma do valor de A com o valor atual de C

